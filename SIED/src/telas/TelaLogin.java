    public void logar() {
        String sql = "select * from tb_usuario where login = ? and senha = ?";

        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtLogin.getText());
            pst.setString(2, txtSenha.getText());
            rs = pst.executeQuery();

            if (rs.next()) {
                String perfil = rs.getString(3);

                if (perfil.equals("Admin")) {

                    TelaPrincipal principal = new TelaPrincipal();
                    principal.setVisible(true);
                    TelaPrincipal.txtUsuario.setText(rs.getString(2));
                    TelaPrincipal.txtUsuario.setForeground(Color.red);
                    this.dispose();

                } else {
                    TelaPrincipal principal = new TelaPrincipal();
                    principal.setVisible(true);
                    TelaPrincipal.txtUsuario.setText(rs.getString(2));
                    this.dispose();
                }

            } else {
                JOptionPane.showMessageDialog(null, "Login e/ou Senha incorretos");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
     
   
    private void soma_professor() {

        String sql = "select count(*) from tb_professor";

        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery();

            if (rs.next()) {
                String soma = rs.getString("count(*)");
                TelaPrincipal.txtTotalProf.setText(soma);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    

